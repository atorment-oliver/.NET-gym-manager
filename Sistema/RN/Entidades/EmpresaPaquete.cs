using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace RN.Entidades
{
    public class EmpresaPaquete
    {
        #region Atributos
        private decimal costo;
        private int empresaId;
        private int paqueteId;

        #endregion
        #region Propiedades
        public decimal Costo
        {
            get { return costo; }
            set { costo = value; }
        }
        public int EmpresaId
        {
            get { return empresaId; }
            set { empresaId = value; }
        }
        public int PaqueteId
        {
            get { return paqueteId; }
            set { paqueteId = value; }
        }
       
       
        #endregion
        #region Metodos
        public EmpresaPaquete()
        {
            costo = -1;
            empresaId = -1;
            paqueteId = -1;
        }
        public EmpresaPaquete(decimal costo, int empresaId, int paqueteId)
        {
            this.costo = costo;
            this.empresaId = empresaId;
            this.paqueteId = paqueteId;
        }
        #endregion
    }
}
