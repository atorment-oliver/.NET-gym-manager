using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace RN.Entidades
{
    public class GrupoDia
    {
        #region Atributos
        private int id;
        private int grupoId;
        private int diaId;

        #endregion
        #region Propiedades
        public int Id
        {
            get { return id; }
            set { id = value; }
        }
        public int GrupoId
        {
            get { return grupoId; }
            set { grupoId = value; }
        }
        public int DiaId
        {
            get { return diaId; }
            set { diaId = value; }
        }
       
       
        #endregion
        #region Metodos
        public GrupoDia()
        {
            id = -1;
            diaId = -1;
            grupoId = -1;
  
        }
        public GrupoDia(int id, int grupoId, int diaId)
        {
            this.id = id;
            this.grupoId = grupoId;
            this.diaId = diaId;
        }
        #endregion
    }
}
